//package Part2;

public class Tuple{

    private String word;
    private String pos;

    public Tuple(String word, String pos)
    {
        this.word = word;
        this.pos = pos;
    }

    public String getWord()
    {
        return word;
    }

    public String getPos()
    {
        return pos;
    }

    public void setWord(String word)
    {
        this.word = word;
    }
}